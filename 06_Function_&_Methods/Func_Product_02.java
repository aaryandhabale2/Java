public class Func_Product_02 {
    public static int multiply(int a, int b){    // Parameters
        int product =  a * b;
        return product;
    }                                
    public static void main(String args[]){
int a = 4;
int b = 7;
int prod = multiply( a , b);     //Argumemts
System.out.println("a * b :- "+ prod);
    }
}
