public class Assignment_06 {
    public static void main(String[] args) {
       int a = 10;
       int b = 20;

       //Assignment operators

       System.out.println("Addition(a = a + b):"+ (a += b));
       System.out.println("Subtraction(a = a - b):" + (a -= b));
       System.out.println("Multiplication(a = a * b):" + (a *= b));
       System.out.println("Division(a = a / b):" + (a /= b));
       System.out.println("Modulus(a = a % b):" + (a %= b));
    }

}


       