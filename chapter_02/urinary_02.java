public class urinary_02 {
    public static void main(String[] args) {
        int a = 10;
        int b = a++; // Postfix increment, b will be assigned the value of a before incrementing
        int c = ++a; // Prefix increment, c will be assigned the value of a brefore incrementing
        System.out.println("Value of a: " + a);
        System.out.println("Value of b: " + b);
        System.out.println("Value of c: " + c);

    }
}
// a+=b,a-=b,a*=b,a/=b,a%=b - this is assignment operator
