public class Switch_09 {
    public static void main(String args[]) {
        int number = 45;
        switch (number) {
            case 1:
                System.out.println("Samosa");
                break;
            case 2:
                System.out.println("Idali");
                break;
            case 3:
                System.out.println("Dahiwadha");
                break;
            case 4:
                System.out.println("Dhosa");
                break;

            default:
                System.out.println("Wake up!!");
        }
    }
}